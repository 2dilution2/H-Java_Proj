package ch03.ex06;

public class ConditionOperator {

	public static void main(String[] args) {
		// 삼항연산자 : if else 를 축약해서 한 라인으로 사용 하는 구문, 
		//  (조건) ? 참(실행구문) : 거짓(실행구문) ; 
		
		int val1 = (3 > 5) ? 6 : 9;
		System.out.println(val1);
		
		int val2 = (3 < 5) ? 6 : 9;
		System.out.println(val2);
		
		

	}

}
